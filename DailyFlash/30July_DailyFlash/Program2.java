

class Languages {

	public static void main(String[] args) {
	
		char ch = 'J';

		switch(ch) {
		
			case 'C':
				System.out.println("C, C++, C#");
				break;
			case 'F':
				System.out.println("Flutter, FORTRAN");
				break;
			case 'J':
				System.out.println("Java, JavaScript");
				break;
			case 'O':
				System.out.println("Objective C");
				break;
			case 'P':
				System.out.println("Python");
				break;
		}
	}
}
