

class RelationalOperator {

	public static void main(String[] args) {
	
		int x = 15, y = 20;
		boolean ans;

		ans = x < y;
		System.out.println(ans);
		
		ans = x > y;
		System.out.println(ans);
		
		ans = x <= y;
		System.out.println(ans);
		
		ans = x >= y;
		System.out.println(ans);
		
		ans = x == y;
		System.out.println(ans);
		
		ans = x != y;
		System.out.println(ans);
	}
}
