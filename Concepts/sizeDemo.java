


class sizeOf {

	public static void main (String[] args) {
	
		System.out.println(Byte.SIZE/8);		//1Byte
		System.out.println(Short.SIZE/8);		//2Byte
		System.out.println(Integer.SIZE/8);		//4Byte
		System.out.println(Long.SIZE/8);		//8Byte
		System.out.println(Float.SIZE/8);		//4Byte
		System.out.println(Double.SIZE/8);		//8Byte
		System.out.println(Character.SIZE/8);		//2Byte
		//System.out.println(Boolean.SIZE/8);		//JVM Dependent
	}
}
