

class NumberCheck {

	public static void main(String[] args) {
	
		int num = 0;

		if(num == 0)
			System.out.println("Entered Number "+ num +" is Equal to 0");
		else if(num > 0)
			System.out.println("Entered Number "+ num +" is Greater than 0");
		else
			System.out.println("Entered Number "+ num +" is Smaller than 0");
	}
}
